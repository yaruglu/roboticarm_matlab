function x=mgd(q)
L1=0.170;
L2=0.300;
L3=0.260;
L4=0.11;
x=zeros(4,1);
%-------------------------         
%x
x(1)=L2*cos(q(1))*cos(q(2))+L3*cos(q(2)+q(3))*cos(q(1))+L4*cos(q(2)+q(3)+q(4))*cos(q(1));
%y
x(2)=(L2*cos(q(2))+L3*cos(q(2)+q(3))+L4*cos(q(2)+q(3)+q(4)))*sin(q(1));
%z
x(3)=L1+L2*sin(q(2))+L3*sin(q(2)+q(3))+L4*sin(q(2)+q(3)+q(4));
x(4)=q(2)+q(3)+q(4);
%-------------------------
end